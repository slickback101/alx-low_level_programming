/**
 * div - divide two integers
 * @a: firest int
 * @b: second int
 * Return: division result 
 */

int div(int a, int b)
{

	return (a / b);
}

/**
 * mul - multiply two integers
 * @a: firest int
 * @b: second int
 * Return: multiply 
 */

int mul(int a, int b)
{

	return (a * b);
}

/**
 * mod - mod of two integers
 * @a: firest int
 * @b: second int
 * Return: final answer 
 */

int mod(int a, int b)
{

	return (a % b);
}

/**
 * sub - subtract two integers
 * @a: firest int
 * @b: second int
 * Return: subtract the final result
 */

int sub(int a, int b)
{

	return (a - b);
}

/**
 * add - add two integers
 * @a: firest int
 * @b: second int
 * Return: addition 
 */

int add(int a, int b)
{

	return (a + b);
}
